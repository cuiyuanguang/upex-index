function transformI18n(obj) {
  const result = {};
  for (const key in obj) {
    if (obj.hasOwnProperty(key)) {
      for (const k in obj[key]) {
        if (obj[key].hasOwnProperty(k)) {
          result[k] = result[k] || {};
          result[k][key] = obj[key][k];
        }
      }
    }
  }
  return result;
}

